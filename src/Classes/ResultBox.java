package Classes;

public class ResultBox {

    public final int topLeftX;
    public final int topLeftY;

    public final int width;
    public final int height;


    public ResultBox(int X, int Y, int w, int h) {
        this.topLeftX = X;
        this.topLeftY = Y;
        this.width = w;
        this.height = h;
    }
}
